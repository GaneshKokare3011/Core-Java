
public class Concatenation {
	public static void main(String[] args) {
		System.out.println("Ganesh" + 2 + 2);
		System.out.println("Ganesh" + (2 + 2));
		System.out.println(2 + 2 + "Ganesh");
		System.out.println("Ganesh" + -4 + 8);
		System.out.println(2 + 2 + "Ganesh" + (-4 + 8));
		System.out.println(2 + 2 + "Ganesh" + 4 * 3);
	}
}
