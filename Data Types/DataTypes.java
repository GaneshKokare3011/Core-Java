package com.dataTypes;

public class DataTypes {
	public static void main(String[] args) {
		byte age = 21;
		short seatNumber = 12345;
		int pinCode = 411018;
		long adharCardNo = 123456789103l;
		
		float height = 5.7f;
		double salary = 100000.035467;
		
		char symbol = '$';
		boolean passFail = true;
		
		System.out.println("Age: " + age);
		System.out.println("Seat Number: " + seatNumber);
		System.out.println("Pin Code: " + pinCode);
		System.out.println("Adhar Number: " + adharCardNo);
		System.out.println("Height: " + height);
		System.out.println("Salary: " + salary);
		System.out.println("Special Symbol: " + symbol);
		System.out.println("Pass/Fail: " + passFail);
	}
}
