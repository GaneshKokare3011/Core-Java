package com.dataTypes;

public class DataTypesSize {
	public static void main(String[] args) {

		System.out.println("Primitive Data Types Size in Byte");
		System.out.println("The Size of byte is: " + Byte.BYTES + " Byte");
		System.out.println("The Size of short is: " + Short.BYTES + " Byte");
		System.out.println("The Size of int is: " + Integer.BYTES + " Byte");
		System.out.println("The Size of long is: " + Long.BYTES + " Byte");
		System.out.println("The Size of float is: " + Float.BYTES + " Byte");
		System.out.println("The Size of double is: " + Double.BYTES + " Byte");
		System.out.println("The Size of char is: " + Character.BYTES + " Byte");
		System.out.println("The Size of boolean is undefined.");
		System.out.println();
		System.out.println("Primitive Data Types Size in bit");
		System.out.println("The Size of byte is: " + Byte.SIZE + " bit");
		System.out.println("The Size of short is: " + Short.SIZE + " bit");
		System.out.println("The Size of int is: " + Integer.SIZE + " bit");
		System.out.println("The Size of long is: " + Long.SIZE + " bit");
		System.out.println("The Size of float is: " + Float.SIZE + " bit");
		System.out.println("The Size of double is: " + Double.SIZE + " bit");
		System.out.println("The Size of char is: " + Character.SIZE + " bit");
		System.out.println("The Size of boolean is undefined.");
	}
}
