package com.dataTypes;

public class DataTypesRange {
	public static void main(String[] args) {
		
		System.out.println("Primitive Data Types Range");
		System.out.println("byte range between " + Byte.MIN_VALUE + " To " + Byte.MAX_VALUE);
		System.out.println("short range between " + Short.MIN_VALUE + " To " + Short.MAX_VALUE);
		System.out.println("int range between " + Integer.MIN_VALUE + " To " + Integer.MAX_VALUE);
		System.out.println("long range between " + Long.MIN_VALUE + " To " + Long.MAX_VALUE);
		System.out.println("float range between " + Float.MIN_VALUE + " To " + Float.MAX_VALUE);
		System.out.println("double range between " + Double.MIN_VALUE + " To " + Double.MAX_VALUE);
		System.out.println("char range between " + (int) Character.MIN_VALUE + " To " + (int) Character.MAX_VALUE);
		System.out.println("boolean range is undefined.");
	}
}
